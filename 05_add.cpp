#include <iostream>
using namespace std;
int main()
{
    int num1, num2, sum;
    cout << "enter num1 : ";
    cin >> num1; // 12
    cout << "enter num2 : ";
    cin >> num2; // 5
    sum = num1 + num2;
    cout << "sum of " << num1 << " and " << num2 << " = " << sum;
    // sum of 12 and 5 = 17
    return 0;
}